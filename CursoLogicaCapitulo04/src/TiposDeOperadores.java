/*
Tipos de operadores

[+ e -] = Adição e Subtração

[* e /] = Multiplicação e Divisão

[%] = Módulo (Resto da divisão)

[> e >=] = Maior que e Maior ou igual a

[< e <=] = Menor que e Menor ou igual a

[== e !=] = Igual e Diferente

[=] = Atribui um valor para a variavel

[+= e -+] = Faz a operação (adição ou subtração) e depois atribue a variavel

[*= e /=] = Faz a operação (multiplicação ou divisão) e depois atribue a variavel

[%=] = Faz a operação (modulo) e depois atribue a variavel

[&&] = Os dois lados do operador devem ser true

[||] = Um dos lados deve ser verdadeiro

[!] = reverte o valor

 */